#include "ext_op.h"

int		res_reg8(struct gb_cpu_s* gb, int bit, uint8_t* reg)
{
	*reg &= ~(1 << bit);
}

int		res0_a(struct gb_cpu_s* gb)
{
	res_reg8(gb, 0, &(gb->reg.a));
}