#include "gb.h"

/*
** LD = 0x40 => 0x7F (excluding 0x76)
*/

void	ld_reg(uint8_t* dst, uint8_t* src)
{
	*dst = *src;
}

void	ld_dc(struct gb_cpu_s* gb)
{
	ld_reg(&(gb->reg.d), &(gb->reg.c));
}