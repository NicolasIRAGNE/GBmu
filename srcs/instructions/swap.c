#include "ext_op.h"

int		swap_n(uint8_t* x)
{
	*x = ((*x & 0xf0) >> 4) | ((*x & 0x0f) << 4);
}

int		swap_a(struct gb_cpu_s* gb)
{
	swap_n(&(gb->reg.a));
}